       Обследования.Название AS [Название обследования]
FROM [Врачи и обследования]
JOIN Врачи ON [Врачи и обследования].[Идентификатор врача] = Врачи.ID
JOIN Обследования ON [Врачи и обследования].[Идентификатор обследования] = Обследования.ID;

