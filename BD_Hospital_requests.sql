SELECT Спонсоры.Название AS [Название спонсора],
Пожертвования.Сумма,
Отделения.Название AS [Название отделения]
FROM Пожертвования
JOIN Спонсоры ON Пожертвования.[Идентификатор спонсора] = Спонсоры.ID
JOIN Отделения ON Пожертвования.[Идентификатор отделения] = Отделения.ID;

